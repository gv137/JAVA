
public class Calculo {

public double CalcularMedia(double n1, double n2){
    return(n1+n2)/2;
    
}
    
public int MaiorNumero(int num1, int num2) {
    if (num1 > num2){
        return num1;
    } else {
        return num2;
    }
}
    
public boolean VerificaPar (int numero){
    return numero%2 == 0;
}

}
